# STRING INPUT
ip = input("Enter a String: ")
print ('You have entered: ', ip)

# INTEGER INPUT
a = int(input("Enter 1st number: "))
b = int(input("Enter 2nd number: "))
c = a + b
print ('Result = ', c)

# CHARACTER INPUT
chr = input("Enter a character: ")[0]
print ('You have entered: ', chr)

# EXPRESSION INPUT
exp = eval(input("Enter an expression: "))
print ('Result = ', exp)
